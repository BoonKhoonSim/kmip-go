func write(def EnumDef) {
	nname := kmiputil.NormalizeName(def.Name)

	fp, err := filepath.Abs("enum_" + strings.ToLower(nname) + ".go")
	if err != nil {
		panic(err)
	}

	fmt.Println(fp)

	f, err := os.Create(fp)
	if err != nil {
		panic(err)
	}
	defer f.Close()

	s := gen(def)
	f.WriteString(s)
}

func gen(d EnumDef) string {
	buf := bytes.NewBuffer(nil)

	tmpl, err := template.New("boilerplate").Parse(enumTmpl)
	if err != nil {
		panic(err)
	}

	input := struct {
		EnumDef
		Var            string
		TypeName string
		Cmd            string
	}{
		EnumDef:        d,
		TypeName: kmiputil.NormalizeName(d.Name),
		Cmd:            strings.Join(os.Args[1:], " "),
	}
	input.Var = strings.ToLower(string([]rune(input.TypeName)[:1]))

	// normalize all the value names
	newValues := make(map[string]uint32, len(d.Values))
	for key, value := range d.Values {
		newValues[kmiputil.NormalizeName(key)] = value
	}
	input.EnumDef.Values = newValues

	err = tmpl.Execute(buf, input)

	if err != nil {
		panic(err)
	}

	//fmt.Fprintf(buf, stringerMeth, d.name)

	// format returns the gofmt-ed contents of the Generator's buffer.

	src, err := format.Source(buf.Bytes())
	if err != nil {
		// Should never happen, but can arise when developing this code.
		// The user can compile the output to see the error.
		log.Printf("warning: internal error: invalid Go generated: %s", err)
		log.Printf("warning: compile the package to analyze the error")
		return buf.String()
	}
	return string(src)
}

func genTag(d map[string]uint32) string {
	buf := bytes.NewBuffer(nil)

	tmpl, err := template.New("boilerplate").Parse(tagTmpl)
	if err != nil {
		panic(err)
	}

	input := make(map[string]uint32, len(d))

	// normalize all the value names
	for key, value := range d {
		input[kmiputil.NormalizeName(key)] = value
	}

	inputs := struct {
		Values map[string]uint32
		Cmd string
	}{
		Values:input,
		Cmd:     strings.Join(os.Args[1:], " "),
	}


	err = tmpl.Execute(buf, inputs)

	if err != nil {
		panic(err)
	}

	//fmt.Fprintf(buf, stringerMeth, d.name)

	// format returns the gofmt-ed contents of the Generator's buffer.

	src, err := format.Source(buf.Bytes())
	if err != nil {
		// Should never happen, but can arise when developing this code.
		// The user can compile the output to see the error.
		log.Printf("warning: internal error: invalid Go generated: %s", err)
		log.Printf("warning: compile the package to analyze the error")
		return buf.String()
	}
	return string(src)
}

